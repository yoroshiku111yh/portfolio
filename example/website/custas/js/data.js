function pushArrayAnimation(arrayPush , item , nameItem , levelOfItem)
{
    arrayAnimationDetail = [];
    arrayAnimationDetail.push(item);
    $.each(arrayPush , function(key , value){
        var Effect = new PIXI.Sprite.fromImage(value.url);
        ratioOfWidthHeightItem(Effect , 1600);
        anchor(Effect);
        Effect.position.x = value.positionX;
        Effect.position.y = value.positionY;
        Effect.done = value.done;
        Effect.last = value.last;
        visible(Effect , 0);
        arrayAnimationDetail.push(Effect);
        contain_item.addChild(Effect);
    });

    arrayAnimation.push({ 'id' : idArrayAnimation , 'level' : levelOfItem , 'item' : nameItem , 'animation' : arrayAnimationDetail});
    //console.log(arrayAnimation);
    idArrayAnimation++;
}

