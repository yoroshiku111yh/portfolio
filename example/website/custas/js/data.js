function findIdInArray(arrayAnimationFind , levelOfItem , nameItem)
{
    var idFind = 0;
    for(var z = 0 ; z < arrayAnimationFind.length ; z++)
    {
        if(arrayAnimationFind[z].level == levelOfItem  && arrayAnimationFind[z].item == nameItem)
        {
            //console.log(z+arrayAnimationFind[z].item);
            idFind = arrayAnimationFind[z].id;
            return idFind;
        }
    }
    //console.log(idFind);
}

function runEffect(idObject) /*update front end*/
{
    var length = arrayAnimation[idObject].animation.length;
    var arrayDetail = arrayAnimation[idObject].animation;
    var IdeffectInUsing = 0;

    if(!arrayDetail[length - 1].back)
    {
        for(var i = 0 ; i < length ; i++ )
        {
            if(!arrayDetail[i].done)
            {
                arrayAnimation[idObject].animation[i].done = true;
                IdeffectInUsing = i;
                for(var j = 0 ; j < length ; j++ )
                {
                    if(j != IdeffectInUsing)
                    {
                        visible(arrayAnimation[idObject].animation[j],0);
                    }
                }
                visible(arrayDetail[i],1);
                return false;
            }
            else if(arrayDetail[i].last)
            {
                arrayAnimation[idObject].animation[i].back = true;
                for (var k = 0 ; k < length ; k++)
                {
                    //if(k != IdeffectInUsing)
                    arrayAnimation[idObject].animation[k].done = false;
                }
                return false;
            }
        }
    }
    else
    {
        for(var i = length-1 ; i >= 0 ; i-- )
        {
            if(!arrayDetail[i].done)
            {
                arrayAnimation[idObject].animation[i].done = true;
                IdeffectInUsing = i;
                for(var j = 0 ; j < length ; j++ )
                {
                    if(j != IdeffectInUsing)
                    {
                        visible(arrayAnimation[idObject].animation[j],0);
                    }
                }

                visible(arrayDetail[i],1);
                return false;
            }
            else if(arrayDetail[0].done)
            {
                arrayAnimation[idObject].animation[length-1].back = false;
                for (var k = 0 ; k < length ; k++)
                {
                    //if(k != IdeffectInUsing)
                    arrayAnimation[idObject].animation[k].done = false;
                }
                return false;
            }

        }
    }
}

